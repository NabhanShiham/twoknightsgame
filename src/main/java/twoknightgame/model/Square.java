package twoknightgame.model;

/**
 * Represents the possible states of a square on the Two Knight game board.
 */
public enum Square {
    /**
     * Represents a square occupied by the white knight.
     */
    WHITEKNIGHT,

    /**
     * Represents a square occupied by the black knight.
     */
    BLACKKNIGHT,

    /**
     * Represents an empty square.
     */
    EMPTY
}